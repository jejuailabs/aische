/** 조립 가능한 블록 목록 (테스트와 문서에서 참조) */
export const BLOCKS = {
  CORE,
  DECOMPOSE,
  TIME_UNSPECIFIED,
  UNCERTAIN,
  TOPIC,
  PERSON,
  CONVERSE,
} as const;

export type BlockName = keyof typeof BLOCKS;

/**
 * 시스템 프롬프트 조립기.
 *
 * CORE는 항상 맨 앞에 들어간다 — 빼먹을 수 없게 하려고 인자로 안 받는다.
 *
 * @param intro   이 라우트가 무엇을 하는지 (역할·오늘 날짜·사용자 데이터 등)
 * @param blocks  추가로 실을 원칙 블록
 * @param outro   이 라우트에만 해당하는 지침과 출력 형식
 */
export function composeSystem(
  intro: string,
  blocks: BlockName[],
  outro: string
): string {
  return [intro.trim(), CORE, ...blocks.map((b) => BLOCKS[b]), outro.trim()]
    .filter(Boolean)
    .join("\n\n");
}
