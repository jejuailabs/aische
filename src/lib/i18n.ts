/**
 * `ko`는 `as const`라 값이 리터럴 타입("저장")으로 고정된다.
 * 그대로 Record<Locale, typeof ko>로 쓰면 en.common.save가 "저장"이어야 해서 컴파일이 안 된다.
 * 아래 Widen은 **키 구조는 그대로 검사하되 문자열 값 타입만 넓힌다.**
 * → en에 키가 빠지면 여전히 컴파일 에러로 잡힌다.
 */
type Widen<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
    ? readonly Widen<U>[]
    : { readonly [K in keyof T]: Widen<T[K]> };

export type TranslationKeys = Widen<typeof ko>;
export type Locale = "ko" | "en";

const translations: Record<Locale, TranslationKeys> = { ko, en };

export function getTranslation(locale: Locale): TranslationKeys {
  return translations[locale] ?? translations.ko;
}

export default translations;